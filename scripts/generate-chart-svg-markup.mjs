import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";

const root = process.cwd();

// Sanitize logic must match src/components/charts/sanitizeChartSvg.ts (build has no TS import).
function sanitizeChartSvg(svg) {
  const withoutForeign = svg.replace(
    /<foreignObject[\s\S]*?<\/foreignObject>/gi,
    "",
  );

  return ensureSvgRootAttributes(withoutForeign);
}

function parseViewBoxSize(viewBox) {
  const parts = viewBox.trim().split(/[\s,]+/).map(Number);

  if (parts.length !== 4 || parts.some((value) => Number.isNaN(value))) {
    return null;
  }

  return { width: parts[2], height: parts[3] };
}

function readSvgAttribute(attrs, name) {
  const match = attrs.match(new RegExp(`\\b${name}=["']([^"']*)["']`, "i"));

  return match?.[1];
}

function ensureSvgRootAttributes(svg) {
  return svg.replace(/<svg\b([^>]*)>/i, (_, rawAttrs) => {
    const viewBox = readSvgAttribute(rawAttrs, "viewBox");
    const parsed = viewBox ? parseViewBoxSize(viewBox) : null;

    let width = readSvgAttribute(rawAttrs, "width");
    let height = readSvgAttribute(rawAttrs, "height");

    if (parsed) {
      width = width ?? String(parsed.width);
      height = height ?? String(parsed.height);
    }

    const resolvedViewBox =
      viewBox ?? (width && height ? `0 0 ${width} ${height}` : "0 0 100 100");

    if (!width || !height) {
      const fallback = parseViewBoxSize(resolvedViewBox);
      width = width ?? String(fallback?.width ?? 100);
      height = height ?? String(fallback?.height ?? 100);
    }

    const passthrough = rawAttrs
      .replace(
        /\s*(xmlns|viewBox|width|height|preserveAspectRatio)=["'][^"']*["']/gi,
        "",
      )
      .trim();

    return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="${resolvedViewBox}" width="${width}" height="${height}" preserveAspectRatio="xMidYMid meet"${passthrough ? ` ${passthrough}` : ""}>`;
  });
}

const charts = [
  { id: "bellCurve", file: "Bell_curve_Chart.svg" },
  { id: "container", file: "Container.svg" },
  { id: "feature", file: "Feature_Container.svg" },
  { id: "halfBlocks", file: "Half_Blocks.svg" },
  { id: "scatter", file: "Scatterplot_Chart.svg" },
  { id: "scatterBar", file: "Scatterplot_Chart_bar.svg" },
];

const entries = charts.map(({ id, file }) => {
  const svgPath = path.join(root, "src/assets", file);
  const svg = sanitizeChartSvg(readFileSync(svgPath, "utf8"));

  return `  ${id}: ${JSON.stringify(svg)},`;
});

const output = `/* eslint-disable */\n/* Auto-generated by scripts/generate-chart-svg-markup.mjs */\n\nexport const chartSvgMarkup = {\n${entries.join("\n")}\n} as const;\n\nexport type ChartSvgId = keyof typeof chartSvgMarkup;\n`;

writeFileSync(
  path.join(root, "src/components/charts/chartSvgMarkup.generated.ts"),
  output,
  "utf8",
);

